    public InsertProfissao(int n) {
        initComponents();
        p = (Profissao) ((ArrayList) ProfissaoDao.getInstance().loadProfissoes()).get(n);
        especialidades = p.getEspecialidades();
        DefaultTableModel dtm = new DefaultTableModel(null, col);
        for(int i=0; i<especialidades.size(); i++){
            dtm.addRow(new Object[]{especialidades.get(i)});
        }
        jTable1.setModel(dtm);
        jTextField1.setText(p.getNome());
        jTextField1.setEnabled(false);
        jButton2.setText("Atualizar");
    }
    
